#!/usr/bin/env python3
"""Generate src/nlEvalRun.js from an NL→OQO eval-run artifact (oxjob #344, #345).

The NL eval pipeline lives in the oxjobs job dir (#344
nl-to-oqo-pipeline/work/run_eval.py). Each run emits a timestamped JSON artifact
(work/eval_runs/eval_<stamp>.json): overall + per-stratum pass rates, the
deciding-tier distribution, and one record per NL formulation (predicted OQO vs
gold OQO, pass/fail, deciding tier, reason). This script copies the latest such
artifact into the GUI as a plain JS module (like src/oqlCorpus.js / src/countries.js)
so the OQL playground's "NL evals" subpage can render it with no runtime backend
call. The fail list is the key view: a human reads pred-vs-gold to tell real model
errors from bad/ambiguous golds.

It is a READ-ONLY snapshot of one run. Editing golds from the GUI is a separate,
deferred decision (#344 PLAN action 1) — this just bakes in what `run_eval.py`
produced so it's auditable in the browser.

Regenerate (run from the openalex-gui repo root):

    python3 scripts/gen_nl_eval.py                 # newest eval_*.json in the job dir
    python3 scripts/gen_nl_eval.py path/to/eval.json   # a specific artifact

Then commit the regenerated src/nlEvalRun.js. Re-run whenever a fresh eval lands.
"""
import glob
import json
import os
import sys

HERE = os.path.dirname(os.path.abspath(__file__))
GUI_ROOT = os.path.dirname(HERE)
# The #344 job dir is a sibling of the repos under ~/Documents.
DEFAULT_RUNS_DIR = os.path.normpath(os.path.join(
    GUI_ROOT, "..", "oxjobs", "working", "nl-to-oqo-pipeline", "work", "eval_runs"))
OUT_JS = os.path.join(GUI_ROOT, "src", "nlEvalRun.js")

# Haiku 4.5 rates ($/Mtok): input, output, cache-read (0.1x), cache-write (1.25x).
RATES = {"in": 1.0, "out": 5.0, "cache_read": 0.10, "cache_write": 1.25}


def _warm_cost_usd(u):
    """Steady-state warm-cache cost (treat one-time cache creation as a read)."""
    return (u["input_tokens"] * RATES["in"]
            + u["output_tokens"] * RATES["out"]
            + (u["cache_read_input_tokens"] + u["cache_creation_input_tokens"])
            * RATES["cache_read"]) / 1e6


def pick_artifact(arg):
    if arg:
        if not os.path.exists(arg):
            sys.exit(f"ERROR: artifact not found: {arg}")
        return arg
    if not os.path.isdir(DEFAULT_RUNS_DIR):
        sys.exit(f"ERROR: default runs dir not found: {DEFAULT_RUNS_DIR}\n"
                 "Pass an artifact path explicitly.")
    candidates = glob.glob(os.path.join(DEFAULT_RUNS_DIR, "eval_*.json"))
    if not candidates:
        sys.exit(f"ERROR: no eval_*.json in {DEFAULT_RUNS_DIR}")
    # Prefer the most recently modified — full runs are written last.
    return max(candidates, key=os.path.getmtime)


def main():
    src = pick_artifact(sys.argv[1] if len(sys.argv) > 1 else None)
    with open(src) as fh:
        run = json.load(fh)

    records = run["records"]

    # Cost summary (warm) from per-record usage, then drop usage to keep the
    # module lean — the viewer only needs the headline numbers.
    usages = [r["usage"] for r in records if r.get("usage")]
    cost = None
    if usages:
        warm = sum(_warm_cost_usd(u) for u in usages) / len(usages)
        cache_hits = sum(1 for u in usages if u.get("cache_read_input_tokens", 0) > 0)
        cost = {
            "warm_cents_per_query": round(warm * 100, 4),
            "cache_hit": cache_hits,
            "n": len(usages),
        }

    # Stable opaque handle per NL formulation: "<case_id>.<n>", n = 1-based
    # position within its case (in run/nl_eval order). Lets us reference a single
    # formulation (e.g. "4.3") without quoting its text; no semantics encoded.
    # run_eval.py now stamps `formulation_id` natively (oxjob #344 Phase 2); fall
    # back to deriving it here for older artifacts that predate that.
    slim = []
    case_counts = {}
    for r in records:
        cid = r["case_id"]
        n = case_counts.get(cid, 0) + 1
        case_counts[cid] = n
        slim.append({
            "case_id": cid,
            "formulation_id": r.get("formulation_id") or f"{cid}.{n}",
            "text": r["text"],
            "difficulty": r["difficulty"],
            "exempt": r.get("exempt"),  # reason string if excluded from the score, else None
            "strata": r["strata"],
            "gold_oqo": r["gold_oqo"],
            "pred_oqo": r.get("pred_oqo"),
            "passed": r["passed"],
            "deciding_tier": r["deciding_tier"],
            "reason": r.get("reason"),
            "n_resolves": r.get("n_resolves"),
        })

    payload = {
        "stamp": run.get("stamp"),
        "model": run.get("model"),
        "overall": run["overall"],
        "by_stratum": run["by_stratum"],
        "deciding": run["deciding"],
        "cost": cost,
        "records": slim,
    }

    body = json.dumps(payload, indent=2)
    n = payload["overall"]["n"]
    npass = payload["overall"]["pass"]
    header = (
        "// AUTO-GENERATED by scripts/gen_nl_eval.py -- do not edit by hand.\n"
        "// Source of truth: oxjobs #344 nl-to-oqo-pipeline/work/eval_runs/ (run_eval.py).\n"
        "// One NL→OQO eval run: overall + per-stratum pass rates, deciding-tier\n"
        "// distribution, and one record per NL formulation (predicted vs gold OQO,\n"
        "// pass/fail, deciding tier). Read-only snapshot; regenerate after a fresh run.\n"
        f"// run stamp: {payload['stamp']}; model: {payload['model']}; "
        f"{npass}/{n} pass.\n\n"
        "export const nlEvalRun = "
    )
    with open(OUT_JS, "w") as fh:
        fh.write(header + body + ";\n")

    print(f"Wrote {OUT_JS}")
    print(f"  from: {src}")
    print(f"  {npass}/{n} pass; {len(slim)} records; model {payload['model']}")
    if cost:
        print(f"  warm cost ≈ {cost['warm_cents_per_query']}¢/query "
              f"(cache hit {cost['cache_hit']}/{cost['n']})")


if __name__ == "__main__":
    main()
